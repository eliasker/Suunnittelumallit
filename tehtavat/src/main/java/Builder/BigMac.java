package Builder;

public class BigMac {
  private String meat = "";
  private String bun = "";
  private String cheese = "";

  public String getMeat() {
    return meat;
  }

  public String getCheese() {
    return cheese;
  }

  public void setCheese(String cheese) {
    this.cheese = cheese;
  }

  public String getBun() {
    return bun;
  }

  public void setBun(String bun) {
    this.bun = bun;
  }

  public void setMeat(String meat) {
    this.meat = meat;
  }

}